//! Vanilla tag bindings per registry, enumerated from the decompiled 26.2
//! data pack (`data/minecraft/tags/`) and sent in `ClientboundUpdateTags`
//! during configuration.
//!
//! Why every listed tag must be present: the client loads *all* synchronized
//! registries from its local pack, and the ones decoded with the full
//! DIRECT_CODEC (enchantment, sulfur_cube_archetype, …) resolve item/block
//! tag references during parsing. Any referenced tag must *exist* over the
//! network or the client aborts configuration with "Missing tag".
//!
//! Tag CONTENTS are the numeric registry ids of each member:
//!   * `minecraft:block` tags carry **block** ids — the registration order of
//!     `Blocks.java` (`BuiltInRegistries.BLOCK.getId`), which is distinct from
//!     the block-*state* ids used by the chunk palette and from item ids.
//!   * `minecraft:item` tags carry **item** ids — the registration order of
//!     `Items.java` (`BuiltInRegistries.ITEM.getId`).
//!
//! Ids were transcribed (clean-room) from the field-declaration order of those
//! two classes; `#tag` references in the JSON are expanded transitively and
//! de-duplicated, preserving first-seen order. Registries other than block/item
//! keep their tags bound **empty** (a valid empty `HolderSet`) — their ids
//! would come from registries we do not yet enumerate.
//!
//! This file is generated — see the commit that introduced the generator.

