#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn lookups_round_trip() {
        // player entity type id was previously a magic number (156).
        assert_eq!(ENTITY_TYPE.id_of("minecraft:player"), Some(156));
        assert_eq!(ENTITY_TYPE.id_of("player"), Some(156)); // bare path
        assert_eq!(ENTITY_TYPE.name_of(0), Some("minecraft:acacia_boat"));
        assert_eq!(MENU.id_of("minecraft:generic_9x1"), Some(0));
        assert_eq!(FLUID.name_of(0), Some("minecraft:empty"));
        assert_eq!(SOUND_EVENT.len(), 1968);
        assert_eq!(ENTITY_TYPE.id_of("minecraft:not_real"), None);
        assert_eq!(ENTITY_TYPE.name_of(-1), None);
    }

    #[test]
    fn every_entry_round_trips() {
        for reg in ALL {
            for (i, name) in reg.entries.iter().enumerate() {
                assert_eq!(reg.id_of(name), Some(i as i32));
                assert_eq!(reg.name_of(i as i32), Some(*name));
            }
        }
    }
}

