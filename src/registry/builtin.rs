//! Flat `BuiltInRegistries` id-tables (name ↔ numeric id).
//!
//! Generated from the vanilla server data report (`--reports` →
//! `generated/reports/registries.json`): for each registry, an entry's numeric
//! id is its `protocol_id`, which every holder-registry `StreamCodec` writes on
//! the wire as a plain VarInt. All of these registries are contiguous `0..n-1`,
//! so an entry's id is its index in the table (mirroring [`super::item`], which
//! keeps its own table because it also carries per-item stack sizes).
//!
//! Datapack-driven registries (biome, damage_type, enchantment, the variants, …)
//! are *not* here — those are network-synced by id-only passthrough in
//! [`super::SYNCED`]. This module is only the code-defined `BuiltInRegistries`.

/// A flat registry: `namespace:path` entries indexed by numeric id.
pub struct Registry {
    /// The registry's own id, e.g. `minecraft:entity_type`.
    #[allow(dead_code)] // provenance/label; read by callers wiring packets by registry.
    pub id: &'static str,
    entries: &'static [&'static str],
}

impl Registry {
    /// Reverse lookup: numeric id → `namespace:path`, if in range.
    #[allow(dead_code)] // lookup API for callers encoding/decoding by id.
    pub fn name_of(&self, id: i32) -> Option<&'static str> {
        usize::try_from(id).ok().and_then(|i| self.entries.get(i).copied())
    }

    /// Look up an entry's numeric id by `namespace:path`. A bare `path` (no `:`)
    /// is assumed to be in the `minecraft` namespace, matching `Identifier`.
    #[allow(dead_code)] // lookup API for callers encoding/decoding by name.
    pub fn id_of(&self, name: &str) -> Option<i32> {
        let owned;
        let full = if name.contains(':') {
            name
        } else {
            owned = format!("minecraft:{name}");
            &owned
        };
        self.entries.iter().position(|n| *n == full).map(|i| i as i32)
    }

    /// The number of entries in the registry.
    #[allow(dead_code)]
    pub fn len(&self) -> usize {
        self.entries.len()
    }
}

